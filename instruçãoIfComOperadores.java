public class instruçãoIfComOperadores {

    public static void main(String[] args) {
        
        int valor = 9;

        if (valor==8) {
            System.out.println("Valor = 8");
        }
        else
            if (valor>8) {
                System.out.println("Valor > 8");
            }
             else{
                System.out.println("Valor < 8");   
            }

    }
}