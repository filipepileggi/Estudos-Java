public class operadorDeModulo {

    public static void main (String[] args) {

        int x = 15;
        int int_resto = x % 10;
        System.out.println("Resultado de 15 % 10=" + int_resto);
        double d = 15.25;
        double double_resto = d % 10;
        System.out.println("Resultado de 15.25 % 10=" + double_resto);
    }
}